package day3_variables;

public class VariablesNamingRule {

	public static void main(String[] args) {
		
		//Variable must start with $ or _ or alphabet letter
		
		//DataType variableName = value;
		
		String salary;
		String $alary;
		String _salary;
		
		//No space on variablesName
		//No special character inside variablesName
		
		int speed = 60;
		int SPEED = 55;
		int Speed = 35;
		
		System.out.println(speed);
		System.out.println(SPEED);
		
		speed = 65;
		System.out.println(speed);
		
		final  int DAYS_IN_WEEK = 7;
		
		System.out.println(DAYS_IN_WEEK);
		
		//int new;
		
		String url = "www.techcircleschool.com"; //www.techcircleschool.com
		
		String username = "Anuwat";
		
		String password = "password";
		
		System.out.println(url);
		
		
		

	}

}
