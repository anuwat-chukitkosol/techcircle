package day4_operators;

public class Relationaloperators {

	public static void main(String[] args) {

		int a = 3;
		
		int b = 4;
		
		int c = 2;
		
		int d = 3;
		
		// == , !=, >=, <=, >, <
		
		System.out.println(a == d);
		
		boolean result = a == d;
		
		System.out.println(result);
		
		System.out.println(a > b);
		
		result = a > b;
		
		System.out.println(result);
		
		System.out.println(a != b);
		
		System.out.println(a >= b);
		
		System.out.println(a <= b);
		
		System.out.println(a != d);
		
		System.out.println(a >= d);
		
		System.out.println(a <= d);
		
		System.out.println(a + c > b);
		
		System.out.println(a + c < b);
		
		result = a * b > c;
		
		System.out.println(result);
		
		
		
		
		
		
		
		
		

	}

}
