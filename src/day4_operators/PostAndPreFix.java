package day4_operators;

public class PostAndPreFix {

	public static void main(String[] args) {

		int numA = 10;
		numA++;
		System.out.println(numA);
		
		System.out.println(numA++);
		
		System.out.println(numA);
		
		//Prefix --> before the variable name
		
		int a = 10;
		
		++a;
		
		System.out.println(a);
		
		System.out.println(++a);
		
		System.out.println(--a);
		System.out.println(a);
		
		System.out.println(a--);
		System.out.println(a);
		
		
		

	}

}
